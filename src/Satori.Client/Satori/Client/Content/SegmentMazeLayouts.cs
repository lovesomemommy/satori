    private static List<TilePoint> ParseWalls(string[] rows)
    {
        var walls = new List<TilePoint>();
        for (var y = 0; y < rows.Length; y++)
        {
            var row = rows[y];
            for (var x = 0; x < row.Length; x++)
            {
                if (row[x] == '#')
                {
                    walls.Add(new TilePoint(x, y));
                }
            }
        }

        return walls;
    }
