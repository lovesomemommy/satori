/// <summary>
/// Лабиринты для 5 тrop (18×11). # — стена, . — проход.
/// </summary>
public static class SegmentMazeLayouts
{
    public static List<TilePoint> GetWalls(int segmentIndex)
    {
        var rows = segmentIndex switch
        {
            0 => Segment0,
            1 => Segment1,
            2 => Segment2,
            3 => Segment3,
            4 => Segment4,
            _ => Segment0
        };

        return ParseWalls(rows);
    }

